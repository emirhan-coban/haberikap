class NewsInfo {
  final String title;
  final String publishDate;
  final String companyName;
  final String summary;
  final String stockCodes;
  final int link;

  NewsInfo({
    required this.title,
    required this.publishDate,
    required this.companyName,
    required this.summary,
    required this.stockCodes,
    required this.link,
  });
}
